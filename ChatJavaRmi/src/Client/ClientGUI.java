    private void jButtonConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConnectActionPerformed
        if (jTextUsername.getText().length() < 1 || localServerInterface!=null) {
            System.out.println("Your conection has failed, you are banned of our system");
        } else {
            nick = jTextUsername.getText();
            password = jTextPassword.getText();
     
        try {
            r = LocateRegistry.getRegistry();
            ClientInterface objExportado = (ClientInterface) new ClientInterfaceImpl(ChatArea,nick);
            ClientInterface stub = (ClientInterface) UnicastRemoteObject.exportObject(objExportado,0);
            r.rebind(nick, stub);
            localServerInterface = (ServerInterface) r.lookup("objeto_servidor");
            if(!localServerInterface.userRegistration(nick,password)){
                localServerInterface=null;
            }
            System.out.println(localServerInterface);
            
        } catch (RemoteException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NotBoundException ex) {
                    Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
        
        
        
        
       }         
    }//GEN-LAST:event_jButtonConnectActionPerformed

    private void jButtonDisconnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDisconnectActionPerformed
            try {
                localServerInterface.disconnect(nick);
                r.unbind(nick);
                localServerInterface = null;
            } catch (RemoteException | NotBoundException ex) {
                Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
            }  
    }//GEN-LAST:event_jButtonDisconnectActionPerformed

